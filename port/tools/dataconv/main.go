// dataconv converts an assembled pokeemerald data object (elf32-littlearm,
// produced by arm-none-eabi-as) into native-pointer C. Each defined symbol
// becomes a packed struct whose fields are byte-runs interleaved with
// pointer-width members at the R_ARM_ABS32 relocation offsets, so 4-byte GBA
// pointers become correct 8-byte host pointers with no interior-label tricks.
package main

import (
	"debug/elf"
	"encoding/binary"
	"fmt"
	"os"
	"sort"
	"strings"
)

// A section-relative reloc (target is a section symbol) is resolved to the
// named symbol containing addend, shifting addend by 4 per intervening pointer
// so it lands at the right host byte after the 4->8 expansion.
type rawReloc struct {
	targetSym  string
	targetSec  int
	addend     uint32
	sectionRel bool
}

type secInfo struct {
	syms       []elf.Symbol
	relocOffs  []uint32
}

func main() {
	if len(os.Args) != 3 {
		fmt.Fprintln(os.Stderr, "usage: dataconv <in.o> <out.c>")
		os.Exit(2)
	}
	f, err := elf.Open(os.Args[1])
	must(err)
	defer f.Close()
	syms, err := f.Symbols()
	must(err)

	inFile := map[string]bool{}
	sec := map[int]*secInfo{}
	bssSec := map[int]bool{}
	for i, s := range f.Sections {
		if s.Flags&elf.SHF_ALLOC == 0 {
			continue
		}
		if s.Type == elf.SHT_PROGBITS {
			sec[i] = &secInfo{}
		} else if s.Type == elf.SHT_NOBITS {
			bssSec[i] = true
		}
	}
	var bss []elf.Symbol
	for _, s := range syms {
		if defined(s) {
			inFile[s.Name] = true
			if si, ok := sec[int(s.Section)]; ok {
				si.syms = append(si.syms, s)
			} else if bssSec[int(s.Section)] {
				bss = append(bss, s)
			}
		}
	}
	relocs := map[int]map[uint32]rawReloc{}
	for i := range sec {
		data, _ := f.Sections[i].Data()
		relocs[i] = readRelocs(f, i, syms, data)
		for off := range relocs[i] {
			sec[i].relocOffs = append(sec[i].relocOffs, off)
		}
		sort.Slice(sec[i].syms, func(a, b int) bool { return sec[i].syms[a].Value < sec[i].syms[b].Value })
		sort.Slice(sec[i].relocOffs, func(a, b int) bool { return sec[i].relocOffs[a] < sec[i].relocOffs[b] })
	}

	var b strings.Builder
	fmt.Fprintf(&b, "// Generated by port/tools/dataconv from %s. Do not edit.\n", os.Args[1])
	fmt.Fprint(&b, "typedef unsigned char u8;\n\n")

	var objects []object
	externs := map[string]bool{}

	// Map header symbols are exactly the targets of gMapGroup_* pointer arrays;
	// they carry no name suffix of their own, so collect them structurally.
	mapHeaders := map[string]bool{}
	for si := range sec {
		data, _ := f.Sections[si].Data()
		locals := sec[si].syms
		for i, s := range locals {
			if !strings.HasPrefix(s.Name, "gMapGroup_") {
				continue
			}
			end := uint32(len(data))
			if i+1 < len(locals) {
				end = uint32(locals[i+1].Value)
			}
			for off, r := range relocs[si] {
				if off < uint32(s.Value) || off >= end {
					continue
				}
				if n := relocTargetName(r, sec); n != "" {
					mapHeaders[n] = true
				}
			}
		}
	}

	var bsLayout map[int][]bsOperand
	if strings.Contains(os.Args[1], "battle_scripts") {
		opNum := parseOpcodeEnum("include/constants/battle_script_commands.h")
		bsLayout = parseBattleScriptLayout("asm/macros/battle_script.inc", "include/constants/battle_script_commands.h")
		interpHost := parseInterpHostLen("src/battle_script_commands.c", opNum)
		relocPtrs := aggregateBattleScriptPtrs(sec, relocs, f, bsLayout)
		classifyBattleScriptPtrs(bsLayout, interpHost, relocPtrs)
	}

	for si := range sec {
		data, _ := f.Sections[si].Data()
		locals := sec[si].syms
		for i, s := range locals {
			start, end := uint32(s.Value), uint32(len(data))
			if i+1 < len(locals) {
				end = uint32(locals[i+1].Value)
			}
			o := object{name: s.Name}
			if sc := schemaFor(s.Name, mapHeaders); sc != nil {
				emitStruct(&o, sc, data, start, end, si, relocs, sec, inFile, externs)
			} else if bsLayout != nil && strings.HasPrefix(s.Name, "BattleScript_") {
				emitBattleScript(&o, bsLayout, data, start, end, si, relocs, sec, inFile, externs)
			} else {
				for pos := start; pos < end; {
					if r, ok := relocs[si][pos]; ok {
						o.slots = append(o.slots, ptrSlot{len(o.bytes), resolve(r, sec, inFile, externs)})
						o.bytes = append(o.bytes, make([]byte, 8)...)
						pos += 4
						continue
					}
					o.bytes = append(o.bytes, data[pos])
					pos++
				}
			}
			objects = append(objects, o)
		}
	}

	var ext []string
	for n := range externs {
		if !inFile[n] {
			ext = append(ext, n)
		}
	}
	sort.Strings(ext)
	for _, n := range ext {
		fmt.Fprintf(&b, "extern u8 %s[];\n", n)
	}
	b.WriteString("\n")
	for _, o := range objects {
		fmt.Fprintf(&b, "extern u8 %s[];\n", o.name)
	}
	for _, s := range bss {
		n := s.Size
		if n == 0 {
			n = 1
		}
		fmt.Fprintf(&b, "u8 %s[%d] __attribute__((weak));\n", s.Name, n)
	}
	b.WriteString("\n")
	for _, o := range objects {
		fmt.Fprintf(&b, "u8 %s[%d] __attribute__((weak)) = {%s};\n", o.name, len(o.bytes), byteList(o.bytes))
	}

	fmt.Fprint(&b, "\nstatic const struct { u8 *base; unsigned off; const void *tgt; } _relocs[] __attribute__((used)) = {\n")
	nrel := 0
	for _, o := range objects {
		for _, sl := range o.slots {
			fmt.Fprintf(&b, "  {%s, %d, %s},\n", o.name, sl.off, sl.expr)
			nrel++
		}
	}
	fmt.Fprint(&b, "};\n")
	if nrel > 0 {
		fmt.Fprint(&b, "__attribute__((constructor)) static void _apply(void){\n")
		fmt.Fprint(&b, "  for (unsigned i=0;i<sizeof(_relocs)/sizeof(_relocs[0]);i++)\n")
		fmt.Fprint(&b, "    __builtin_memcpy(_relocs[i].base+_relocs[i].off,&_relocs[i].tgt,sizeof(void*));\n}\n")
	}
	must(os.WriteFile(os.Args[2], []byte(b.String()), 0644))
	fmt.Fprintf(os.Stderr, "wrote %d objects, %d externs -> %s\n", len(objects), len(ext), os.Args[2])
}

type ptrSlot struct {
	off  int
	expr string
}

// bsOperand is one operand of a battle-script bytecode instruction. A 4-byte
// pointer operand grows to 8 bytes on the host; NULL pointers carry no
// relocation, so only a layout parsed from the command macros can tell dataconv
// to expand them and keep the fixed-stride interpreter in sync.
type bsOperand struct {
	size int
	ptr  bool
	name string
}

// parseInterpHostLen reads the battle-script command table + each handler's
// `gBattlescriptCurrInstr += N` strides to recover the authoritative host
// instruction length per opcode. (host-gba)/4 is then the exact pointer count,
// which disambiguates which .4byte operands grew from 4 to 8 bytes — the macro
// param names alone lie (jump targets named "val", unused operands named "ptr").
func parseInterpHostLen(srcPath string, opNum map[string]int) map[int]int {
	b, err := os.ReadFile(srcPath)
	if err != nil {
		return nil
	}
	src := string(b)
	tbl := map[int]string{}
	if i := strings.Index(src, "gBattleScriptingCommandsTable[]"); i >= 0 {
		rest := src[i:]
		if j := strings.Index(rest, "};"); j >= 0 {
			idx := 0
			for _, line := range strings.Split(rest[:j], "\n") {
				if k := strings.Index(line, "= Cmd_"); k >= 0 {
					name := strings.TrimRight(strings.Fields(line[k+2:])[0], ",")
					tbl[idx] = name
					idx++
				}
			}
		}
	}
	out := map[int]int{}
	for op, fn := range tbl {
		m := strings.Index(src, "static void "+fn+"(void)\n{")
		if m < 0 {
			continue
		}
		depth, j := 1, m+len("static void "+fn+"(void)\n{")
		for j < len(src) && depth > 0 {
			switch src[j] {
			case '{':
				depth++
			case '}':
				depth--
			}
			j++
		}
		body := src[m:j]
		best := 0
		for _, mm := range strings.Split(body, "gBattlescriptCurrInstr += ") {
			var n int
			if _, err := fmt.Sscanf(mm, "%d", &n); err == nil && n > best {
				best = n
			}
		}
		if best > 0 {
			out[op] = best
		}
	}
	return out
}

// classifyBattleScriptPtrs sets ptr flags per operand: relocated .4byte operands
// are pointers, and if the interpreter's host length demands more pointers than
// were relocated (an operand that is NULL in every script), the remaining .4byte
// operands — preferring pointer-named ones — are promoted to fill the count.
func classifyBattleScriptPtrs(layout map[int][]bsOperand, interpHost map[int]int, relocPtrs map[int]map[int]bool) {
	for op, ops := range layout {
		gba := 1
		var four []int
		for i, o := range ops {
			gba += o.size
			ops[i].ptr = false
			if o.size == 4 {
				four = append(four, i)
			}
		}
		chosen := map[int]bool{}
		for _, i := range four {
			if relocPtrs[op][i] {
				ops[i].ptr = true
				chosen[i] = true
			}
		}
		want := -1
		if h, ok := interpHost[op]; ok {
			want = (h - gba) / 4
		}
		if want < 0 {
			for _, i := range four {
				if isPtrArg(ops[i].name) {
					ops[i].ptr = true
					chosen[i] = true
				}
			}
			continue
		}
		for _, preferNamed := range []bool{true, false} {
			for len(chosen) < want {
				pick := -1
				for _, i := range four {
					if chosen[i] {
						continue
					}
					if preferNamed && !isPtrArg(ops[i].name) {
						continue
					}
					pick = i
					break
				}
				if pick < 0 {
					break
				}
				ops[pick].ptr = true
				chosen[pick] = true
			}
		}
	}
}

func parseOpcodeEnum(path string) map[string]int {
	b, err := os.ReadFile(path)
	must(err)
	out := map[string]int{}
	next, in := 0, false
	for _, line := range strings.Split(string(b), "\n") {
		line = strings.TrimSpace(line)
		if strings.HasPrefix(line, "enum ") {
			in = true
			continue
		}
		if !in {
			continue
		}
		if strings.HasPrefix(line, "}") {
			break
		}
		for _, tok := range strings.Split(line, ",") {
			tok = strings.TrimSpace(strings.TrimSuffix(tok, ","))
			if tok == "" || strings.HasPrefix(tok, "{") {
				continue
			}
			name := tok
			if i := strings.IndexByte(tok, '='); i >= 0 {
				name = strings.TrimSpace(tok[:i])
				fmt.Sscanf(strings.TrimSpace(tok[i+1:]), "%v", &next)
			}
			out[name] = next
			next++
		}
	}
	return out
}

func isPtrArg(arg string) bool {
	name := strings.TrimPrefix(arg, "\\")
	if name == "NULL" || name == "0" || name == "src" || name == "dest" {
		return true
	}
	l := strings.ToLower(name)
	return strings.HasSuffix(l, "ptr") || strings.Contains(l, "instr")
}

// parseBattleScriptLayout maps each opcode to its operand layout, merged across
// every macro sharing that opcode (e.g. tryfaintmon / tryfaintmon_spikes) so a
// pointer slot is recognised even when one variant hardcodes NULL.
func parseBattleScriptLayout(macroPath, enumPath string) map[int][]bsOperand {
	opNum := parseOpcodeEnum(enumPath)
	b, err := os.ReadFile(macroPath)
	must(err)
	out := map[int][]bsOperand{}
	var ops []bsOperand
	opcode, sawOpcode := -1, false
	flush := func() {
		if opcode >= 0 {
			if prev, ok := out[opcode]; ok {
				for i := range ops {
					if i < len(prev) && isPtrArg(prev[i].name) && !isPtrArg(ops[i].name) {
						ops[i].name = prev[i].name
					}
				}
				if len(prev) > len(ops) {
					ops = prev
				}
			}
			out[opcode] = ops
		}
	}
	for _, line := range strings.Split(string(b), "\n") {
		f := strings.Fields(line)
		if len(f) == 0 {
			continue
		}
		switch f[0] {
		case ".macro":
			ops, opcode, sawOpcode = nil, -1, false
		case ".endm":
			flush()
			ops, opcode, sawOpcode = nil, -1, false
		case ".byte":
			if !sawOpcode {
				sawOpcode = true
				if len(f) > 1 {
					opcode = opNum[f[1]]
				}
				continue
			}
			ops = append(ops, bsOperand{size: 1})
		case ".2byte":
			ops = append(ops, bsOperand{size: 2})
		case ".4byte":
			nm := ""
			if len(f) > 1 {
				nm = f[1]
			}
			ops = append(ops, bsOperand{size: 4, name: nm})
		}
	}
	return out
}

func aggregateBattleScriptPtrs(sec map[int]*secInfo, relocs map[int]map[uint32]rawReloc, f *elf.File, layout map[int][]bsOperand) map[int]map[int]bool {
	out := map[int]map[int]bool{}
	for si := range sec {
		data, _ := f.Sections[si].Data()
		for _, s := range sec[si].syms {
			if !strings.HasPrefix(s.Name, "BattleScript_") {
				continue
			}
			end := uint32(len(data))
			for _, s2 := range sec[si].syms {
				if uint32(s2.Value) > uint32(s.Value) && uint32(s2.Value) < end {
					end = uint32(s2.Value)
				}
			}
			pos := uint32(s.Value)
			for pos < end {
				op := int(data[pos])
				pos++
				ops, ok := layout[op]
				if !ok {
					break
				}
				for oi, opd := range ops {
					if pos+uint32(opd.size) > end {
						break
					}
					if opd.size == 4 {
						if _, has := relocs[si][pos]; has {
							if out[op] == nil {
								out[op] = map[int]bool{}
							}
							out[op][oi] = true
						}
					}
					pos += uint32(opd.size)
				}
			}
		}
	}
	return out
}

// emitBattleScript walks a script body opcode-by-opcode, expanding pointer
// operands (relocated or NULL) to 8 bytes so the host interpreter's strides hold.
func emitBattleScript(o *object, layout map[int][]bsOperand, data []byte, start, end uint32, si int, relocs map[int]map[uint32]rawReloc, sec map[int]*secInfo, inFile, externs map[string]bool) {
	pos := start
	for pos < end {
		op := int(data[pos])
		o.bytes = append(o.bytes, data[pos])
		pos++
		ops, ok := layout[op]
		if !ok {
			fmt.Fprintf(os.Stderr, "dataconv: unknown battle-script opcode %d in %s; copying tail raw\n", op, o.name)
			for pos < end {
				o.bytes = append(o.bytes, data[pos])
				pos++
			}
			return
		}
		for _, opd := range ops {
			r, hasReloc := relocs[si][pos]
			if opd.size == 4 && (opd.ptr || hasReloc) {
				if hasReloc {
					o.slots = append(o.slots, ptrSlot{len(o.bytes), resolve(r, sec, inFile, externs)})
				}
				o.bytes = append(o.bytes, make([]byte, 8)...)
			} else {
				o.bytes = append(o.bytes, data[pos:pos+uint32(opd.size)]...)
			}
			pos += uint32(opd.size)
		}
	}
}

type object struct {
	name  string
	bytes []byte
	slots []ptrSlot
}

// A schema describes a fixed-layout C struct that the host compiler lays out
// with 8-byte pointer alignment, unlike the ILP32 GBA source. slots gives each
// pointer/union member as {gbaOff, hostOff}; the converter reproduces the exact
// host layout (alignment pad + 4->8 pointer growth) so *(struct*)sym is valid.
type schema struct {
	gbaSize, hostSize int
	slots             [][2]int
	array             bool
}

func relocTargetName(r rawReloc, sec map[int]*secInfo) string {
	if !r.sectionRel {
		return r.targetSym
	}
	si, ok := sec[r.targetSec]
	if !ok || len(si.syms) == 0 {
		return ""
	}
	idx := sort.Search(len(si.syms), func(i int) bool { return uint32(si.syms[i].Value) > r.addend }) - 1
	if idx < 0 {
		return ""
	}
	return si.syms[idx].Name
}

func schemaFor(name string, mapHeaders map[string]bool) *schema {
	switch {
	case mapHeaders[name]:
		return &schema{28, 48, [][2]int{{0, 0}, {4, 8}, {8, 16}, {12, 24}}, false}
	case strings.HasSuffix(name, "_MapEvents"):
		return &schema{20, 40, [][2]int{{4, 8}, {8, 16}, {12, 24}, {16, 32}}, false}
	case strings.HasSuffix(name, "_MapConnections"):
		return &schema{8, 16, [][2]int{{4, 8}}, false}
	case strings.HasSuffix(name, "_ObjectEvents"):
		return &schema{24, 32, [][2]int{{16, 16}}, true}
	case strings.HasSuffix(name, "_MapCoordEvents"):
		return &schema{16, 24, [][2]int{{12, 16}}, true}
	case strings.HasSuffix(name, "_MapBGEvents"):
		return &schema{12, 16, [][2]int{{8, 8}}, true}
	case strings.HasSuffix(name, "_Layout") && !strings.HasSuffix(name, "_Border") && !strings.HasSuffix(name, "_Blockdata"):
		return &schema{24, 40, [][2]int{{8, 8}, {12, 16}, {16, 24}, {20, 32}}, false}
	}
	return nil
}

func emitStruct(o *object, sc *schema, data []byte, start, end uint32, si int, relocs map[int]map[uint32]rawReloc, sec map[int]*secInfo, inFile, externs map[string]bool) {
	n := 1
	if sc.array {
		n = int((end - start)) / sc.gbaSize
	}
	for e := 0; e < n; e++ {
		ebGba := start + uint32(e*sc.gbaSize)
		ebHost := len(o.bytes)
		buf := make([]byte, sc.hostSize)
		prevGba, prevHostEnd := 0, 0
		for _, sl := range sc.slots {
			seg := sl[0] - prevGba
			copy(buf[prevHostEnd:prevHostEnd+seg], data[ebGba+uint32(prevGba):ebGba+uint32(sl[0])])
			copy(buf[sl[1]:sl[1]+4], data[ebGba+uint32(sl[0]):ebGba+uint32(sl[0])+4])
			if r, ok := relocs[si][ebGba+uint32(sl[0])]; ok {
				o.slots = append(o.slots, ptrSlot{ebHost + sl[1], resolve(r, sec, inFile, externs)})
			}
			prevGba = sl[0] + 4
			prevHostEnd = sl[1] + 8
		}
		copy(buf[prevHostEnd:prevHostEnd+(sc.gbaSize-prevGba)], data[ebGba+uint32(prevGba):ebGba+uint32(sc.gbaSize)])
		o.bytes = append(o.bytes, buf...)
	}
}

func resolve(r rawReloc, sec map[int]*secInfo, inFile, externs map[string]bool) string {
	if !r.sectionRel {
		externs[r.targetSym] = true
		if inFile[r.targetSym] {
			return withAddend("(const void *)&"+r.targetSym, r.addend)
		}
		return withAddend("(const void *)"+r.targetSym, r.addend)
	}
	si, ok := sec[r.targetSec]
	if !ok || len(si.syms) == 0 {
		return fmt.Sprintf("(const void *)0 /* unresolved section reloc +%d */", r.addend)
	}
	idx := sort.Search(len(si.syms), func(i int) bool { return uint32(si.syms[i].Value) > r.addend }) - 1
	if idx < 0 {
		idx = 0
	}
	s := si.syms[idx]
	src := r.addend - uint32(s.Value)
	between := uint32(0)
	for _, off := range si.relocOffs {
		if off >= uint32(s.Value) && off < r.addend {
			between++
		}
	}
	host := src + 4*between
	externs[s.Name] = true
	if host == 0 {
		return "(const void *)&" + s.Name
	}
	return fmt.Sprintf("(const void *)((const u8 *)&%s + %d)", s.Name, host)
}

func withAddend(base string, addend uint32) string {
	if addend == 0 {
		return base
	}
	return fmt.Sprintf("(const void *)((const u8 *)%s + %d)", strings.TrimPrefix(base, "(const void *)"), addend)
}

func readRelocs(f *elf.File, secIdx int, syms []elf.Symbol, data []byte) map[uint32]rawReloc {
	out := map[uint32]rawReloc{}
	for _, rs := range f.Sections {
		if rs.Type != elf.SHT_REL || int(rs.Info) != secIdx {
			continue
		}
		rd, _ := rs.Data()
		for off := 0; off+8 <= len(rd); off += 8 {
			roff := binary.LittleEndian.Uint32(rd[off:])
			info := binary.LittleEndian.Uint32(rd[off+4:])
			symIdx := info >> 8
			if symIdx == 0 || int(symIdx) > len(syms) {
				continue
			}
			s := syms[symIdx-1]
			addend := binary.LittleEndian.Uint32(data[roff:])
			if elf.ST_TYPE(s.Info) == elf.STT_SECTION || s.Name == "" {
				out[roff] = rawReloc{targetSec: int(s.Section), addend: addend, sectionRel: true}
			} else {
				out[roff] = rawReloc{targetSym: s.Name, addend: addend}
			}
		}
	}
	return out
}

func byteList(b []byte) string {
	parts := make([]string, len(b))
	for i, by := range b {
		parts[i] = fmt.Sprintf("%d", by)
	}
	return strings.Join(parts, ",")
}

func defined(s elf.Symbol) bool {
	return s.Name != "" && !strings.HasPrefix(s.Name, "$") &&
		s.Section != elf.SHN_UNDEF && s.Section < elf.SHN_LORESERVE &&
		elf.ST_TYPE(s.Info) != elf.STT_SECTION
}

func sanitize(s string) string {
	return strings.Map(func(r rune) rune {
		if r == '_' || (r >= 'a' && r <= 'z') || (r >= 'A' && r <= 'Z') || (r >= '0' && r <= '9') {
			return r
		}
		return '_'
	}, s)
}

