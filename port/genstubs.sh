#!/usr/bin/env bash
set -uo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
PE="$ROOT/pokeemerald"
OBJ="$ROOT/port/build/obj"
GEN="$ROOT/port/build/gen"; mkdir -p "$GEN"
ELF="$PE/pokeemerald_modern.elf"
OUT="$GEN/stubs.c"

rm -f "$OBJ/_hle_stubs.o"
clang -o /dev/null "$ROOT/port/build/_main.o" "$OBJ"/*.o 2>/tmp/_link.err || true
grep -oE '"_[A-Za-z0-9_]+"' /tmp/_link.err | tr -d '"' | sed 's/^_//' | sort -u > /tmp/_undef.txt
arm-none-eabi-nm --defined-only "$ELF" 2>/dev/null > /tmp/_oracle_nm.txt

awk '
  NR==FNR { t[$NF]=$(NF-1); next }
  {
    ty=t[$1]
    if (ty=="t"||ty=="T"||ty=="w"||ty=="W") { print "void " $1 "(void) {}"; f++ }
    else { print "u8 " $1 "[4096];"; d++ }
  }
  END { printf("stubs: %d functions, %d data\n", f, d) > "/dev/stderr" }
' /tmp/_oracle_nm.txt /tmp/_undef.txt > "$OUT.body"

{ echo "// Generated by port/genstubs.sh. Phase-0 stubs (sound + boot)."; echo "typedef unsigned char u8;"; cat "$OUT.body"; } > "$OUT"
rm -f "$OUT.body"
clang -c -std=gnu11 -Wno-everything "$OUT" -o "$OBJ/_hle_stubs.o" 2>/tmp/_stubs.err
echo "compile exit $? ($(grep -c error: /tmp/_stubs.err) errs)"; head -4 /tmp/_stubs.err
