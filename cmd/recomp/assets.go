package main

import (
	"bytes"
	"debug/elf"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

const romBase = 0x08000000

type dataSym struct {
	name string
	addr uint32
	size uint32
}

func loadDataSymbols(path string) ([]dataSym, error) {
	f, err := elf.Open(path)
	if err != nil {
		return nil, err
	}
	defer f.Close()
	syms, err := f.Symbols()
	if err != nil {
		return nil, err
	}
	var out []dataSym
	for _, s := range syms {
		if elf.ST_TYPE(s.Info) != elf.STT_OBJECT || s.Size == 0 || s.Name == "" {
			continue
		}
		a := uint32(s.Value) &^ 1
		if a < romBase || a >= 0x0A000000 {
			continue
		}
		out = append(out, dataSym{s.Name, a, uint32(s.Size)})
	}
	sort.Slice(out, func(i, j int) bool {
		if out[i].addr != out[j].addr {
			return out[i].addr < out[j].addr
		}
		return out[i].size > out[j].size
	})
	return out, nil
}

type asset struct {
	off  uint32 // offset into ROM image
	size uint32
	dir  string
	file string // base name without extension
	ext  string
	pal  bool
}

func classify(name string, size uint32) (dir, ext string, pal bool) {
	has := func(s string) bool { return strings.Contains(name, s) }
	switch {
	case (has("Pal") || has("Palette")) && size%2 == 0 && size <= 0x400:
		return "pal", ".pal", true
	case has("Gfx") || has("Pic") || has("Tiles") || has("Tileset"):
		return "gfx", ".4bpp", false
	case has("Tilemap") || has("_Map") || has("Layout"):
		return "tilemap", ".bin", false
	default:
		return "data", ".bin", false
	}
}

func extractAssets(gbaPath, elfPath, outDir string) error {
	rom, err := os.ReadFile(gbaPath)
	if err != nil {
		return err
	}
	syms, err := loadDataSymbols(elfPath)
	if err != nil {
		return err
	}

	var parts []asset
	usedPath := map[string]bool{}
	uniq := func(dir, name, ext string, off uint32) string {
		base := sanitizeFile(name)
		key := strings.ToLower(dir + "/" + base + ext)
		if usedPath[key] { // offset is unique per part, so this can't re-collide
			base = fmt.Sprintf("%s_%08X", base, romBase+off)
			key = strings.ToLower(dir + "/" + base + ext)
		}
		usedPath[key] = true
		return base
	}

	cursor := uint32(0)
	end := uint32(len(rom))
	addGap := func(to uint32) {
		if to <= cursor {
			return
		}
		parts = append(parts, asset{off: cursor, size: to - cursor, dir: "raw",
			file: fmt.Sprintf("%08X", romBase+cursor), ext: ".bin"})
		cursor = to
	}

	for _, s := range syms {
		off := s.addr - romBase
		if off < cursor { // overlaps an already-placed symbol; skip
			continue
		}
		if off+s.size > end {
			continue
		}
		addGap(off)
		dir, ext, pal := classify(s.name, s.size)
		if pal && !palExact(rom[off:off+s.size]) {
			dir, ext, pal = "data", ".bin", false
		}
		base := uniq(dir, s.name, ext, off)
		parts = append(parts, asset{off: off, size: s.size, dir: dir, file: base, ext: ext, pal: pal})
		cursor = off + s.size
	}
	addGap(end)

	if err := writeAssets(rom, outDir, parts); err != nil {
		return err
	}
	if err := verifyAssets(rom, outDir, parts); err != nil {
		return fmt.Errorf("reassembly verify failed: %w", err)
	}
	named := 0
	for _, p := range parts {
		if p.dir != "raw" {
			named++
		}
	}
	fmt.Printf("assets: %d files (%d named, %d raw gaps), reassembly byte-exact\n",
		len(parts), named, len(parts)-named)
	return nil
}

func sanitizeFile(s string) string {
	var b strings.Builder
	for _, r := range s {
		if r == '_' || r >= 'a' && r <= 'z' || r >= 'A' && r <= 'Z' || r >= '0' && r <= '9' {
			b.WriteRune(r)
		} else {
			b.WriteByte('_')
		}
	}
	return b.String()
}

func writeAssets(rom []byte, outDir string, parts []asset) error {
	filesDir := filepath.Join(outDir, "files")
	for _, d := range []string{"pal", "gfx", "tilemap", "data", "raw"} {
		if err := os.MkdirAll(filepath.Join(filesDir, d), 0755); err != nil {
			return err
		}
	}

	var man bytes.Buffer
	man.WriteString("// Code generated by cmd/recomp. DO NOT EDIT.\n")
	man.WriteString("package assets\n\n")
	man.WriteString("type region struct {\n\tpath string\n\tsize int\n\tpal  bool\n}\n\n")
	man.WriteString("var regions = []region{\n")

	for _, p := range parts {
		data := rom[p.off : p.off+p.size]
		rel := filepath.Join(p.dir, p.file+p.ext)
		full := filepath.Join(filesDir, rel)
		var content []byte
		if p.pal {
			content = encodePal(data)
		} else {
			content = data
		}
		if err := os.WriteFile(full, content, 0644); err != nil {
			return err
		}
		fmt.Fprintf(&man, "\t{%q, %d, %v},\n", "files/"+filepath.ToSlash(rel), p.size, p.pal)
	}
	man.WriteString("}\n")
	return writeGo(filepath.Join(outDir, "manifest.go"), man.Bytes())
}

func palExact(data []byte) bool {
	return bytes.Equal(decodePal(encodePal(data)), data)
}

func encodePal(data []byte) []byte {
	n := len(data) / 2
	var b bytes.Buffer
	fmt.Fprintf(&b, "JASC-PAL\n0100\n%d\n", n)
	for i := 0; i < n; i++ {
		c := uint16(data[i*2]) | uint16(data[i*2+1])<<8
		r := uint8(c & 0x1F)
		gg := uint8(c >> 5 & 0x1F)
		bl := uint8(c >> 10 & 0x1F)
		fmt.Fprintf(&b, "%d %d %d\n", r<<3|r>>2, gg<<3|gg>>2, bl<<3|bl>>2)
	}
	return b.Bytes()
}

func decodePal(text []byte) []byte {
	lines := strings.Split(strings.ReplaceAll(string(text), "\r\n", "\n"), "\n")
	var out []byte
	for _, ln := range lines[3:] {
		ln = strings.TrimSpace(ln)
		if ln == "" {
			continue
		}
		var r, g, bl int
		fmt.Sscanf(ln, "%d %d %d", &r, &g, &bl)
		c := uint16(r>>3) | uint16(g>>3)<<5 | uint16(bl>>3)<<10
		out = append(out, byte(c), byte(c>>8))
	}
	return out
}

func verifyAssets(rom []byte, outDir string, parts []asset) error {
	var img []byte
	for _, p := range parts {
		raw, err := os.ReadFile(filepath.Join(outDir, "files", p.dir, p.file+p.ext))
		if err != nil {
			return err
		}
		if p.pal {
			raw = decodePal(raw)
		}
		if uint32(len(raw)) != p.size {
			return fmt.Errorf("%s: size %d != %d", p.file, len(raw), p.size)
		}
		img = append(img, raw...)
	}
	if !bytes.Equal(img, rom) {
		for i := range img {
			if img[i] != rom[i] {
				return fmt.Errorf("byte mismatch at ROM offset 0x%X (0x%08X)", i, romBase+i)
			}
		}
		return fmt.Errorf("length mismatch: %d != %d", len(img), len(rom))
	}
	return nil
}
