const fnsPerFile = 400

func writeGo(path string, src []byte) error {
	out, err := format.Source(src)
	if err != nil {
		return fmt.Errorf("%s: %w", path, err)
	}
	return os.WriteFile(path, out, 0644)
}

var goKeywords = map[string]bool{
	"break": true, "case": true, "chan": true, "const": true, "continue": true,
	"default": true, "defer": true, "else": true, "fallthrough": true, "for": true,
	"func": true, "go": true, "goto": true, "if": true, "import": true,
	"interface": true, "map": true, "package": true, "range": true, "return": true,
	"select": true, "struct": true, "switch": true, "type": true, "var": true,
}

var reservedIdents = map[string]bool{
	"reg": true, "Install": true, "Entry": true, "g": true,
	"c": true, "e": true, "t": true, "lr": true, "jt": true, "ret": true,
}

var regsIdent = regexp.MustCompile(`^regs\d+$`)

func sanitizeIdent(s string) string {
	var b strings.Builder
	for i, r := range s {
		ok := r == '_' || r >= 'a' && r <= 'z' || r >= 'A' && r <= 'Z' || r >= '0' && r <= '9'
		if i == 0 && r >= '0' && r <= '9' {
			b.WriteByte('_')
		}
		if ok {
			b.WriteRune(r)
		} else {
			b.WriteByte('_')
		}
	}
	return b.String()
}

func pkgIdent(g string) string {
	n := sanitizeIdent(g)
	if n == "" || goKeywords[n] || n == "main" || n == "emerald" {
		n += "_"
	}
	return n
}

func buildNames(a *Analysis) {
	pkgSet := map[string]bool{"emerald": true}
	for _, id := range a.PkgIdent {
		pkgSet[id] = true
	}

	a.Names = map[uint32]string{}
	used := map[string]map[string]bool{}
	for _, fc := range a.Fns {
		addr := fc.Fn.Addr
		pkg := a.Pkg[addr]
		u := used[pkg]
		if u == nil {
			u = map[string]bool{}
			used[pkg] = u
		}
		bad := func(n string) bool {
			return n == "" || goKeywords[n] || reservedIdents[n] || regsIdent.MatchString(n) ||
				pkgSet[n] || u[n] || u[n+"_e"] || strings.HasSuffix(n, "_e") && u[strings.TrimSuffix(n, "_e")]
		}
		base := sanitizeIdent(fc.Fn.Name)
		n := base
		if bad(n) && base != "" {
			n = fmt.Sprintf("%s_%08X", base, addr)
		}
		if bad(n) {
			n = fmt.Sprintf("f_%08X", addr)
		}
		u[n] = true
		u[n+"_e"] = true
		a.Names[addr] = n
	}
}

func emit(a *Analysis, dir string) error {
	buildNames(a)

	byPkg := map[string][]*FnCode{}
	var order []string
	for _, fc := range a.Fns {
		p := a.Pkg[fc.Fn.Addr]
		if len(byPkg[p]) == 0 {
			order = append(order, p)
		}
		byPkg[p] = append(byPkg[p], fc)
	}
	sort.Strings(order)

	for _, p := range order {
		if err := emitPkg(a, dir, p, byPkg[p]); err != nil {
			return fmt.Errorf("package %s: %w", p, err)
		}
	}
	return emitRoot(a, dir, order)
}

func genHeader(w io.Writer, pkg string) {
	fmt.Fprintln(w, "// Code generated by cmd/recomp from pokeemerald_modern.elf. DO NOT EDIT.")
	fmt.Fprintf(w, "package %s\n\n", pkg)
}

func emitRoot(a *Analysis, dir string, order []string) error {
	var w bytes.Buffer
	genHeader(&w, "emerald")
	fmt.Fprintln(&w, "import (")
	fmt.Fprintln(&w, "\tg \"dbut.dev/sapphire/gba\"")
	fmt.Fprintln(&w)
	for _, p := range order {
		fmt.Fprintf(&w, "\t%q\n", a.Module+"/"+a.PkgIdent[p])
	}
	fmt.Fprintln(&w, ")")
	fmt.Fprintln(&w)
	fmt.Fprintln(&w, "func Install() {")
	for _, p := range order {
		fmt.Fprintf(&w, "\t%s.Install()\n", a.PkgIdent[p])
	}
	fmt.Fprintln(&w, "}")
	fmt.Fprintln(&w)
	fmt.Fprintf(&w, "func Entry(c *g.CPU) { c.NCallArm(0x%08X) }\n", a.Im.Entry)
	return writeGo(filepath.Join(dir, "emerald.go"), w.Bytes())
}

func emitPkg(a *Analysis, dir, pkg string, fns []*FnCode) error {
	pid := a.PkgIdent[pkg]
	pdir := filepath.Join(dir, pid)
	if err := os.MkdirAll(pdir, 0755); err != nil {
		return err
	}

	var chunks [][]*FnCode
	for i := 0; i < len(fns); i += fnsPerFile {
		end := i + fnsPerFile
		if end > len(fns) {
			end = len(fns)
		}
		chunks = append(chunks, fns[i:end])
	}

	var w bytes.Buffer
	genHeader(&w, pid)
	fmt.Fprintln(&w, `import g "dbut.dev/sapphire/gba"`)
	fmt.Fprintln(&w)
	fmt.Fprintln(&w, "type reg struct {")
	fmt.Fprintln(&w, "\taddr, size uint32")
	fmt.Fprintln(&w, "\tfn         func(*g.CPU)")
	fmt.Fprintln(&w, "}")
	fmt.Fprintln(&w)
	fmt.Fprintln(&w, "func Install() {")
	for i := range chunks {
		fmt.Fprintf(&w, "\tfor _, r := range regs%d {\n\t\tg.RegisterNative(r.addr, r.size, r.fn)\n\t}\n", i)
	}
	fmt.Fprintln(&w, "}")
	if err := writeGo(filepath.Join(pdir, "pkg.go"), w.Bytes()); err != nil {
		return err
	}

	for ci, chunk := range chunks {
		if err := emitChunk(a, pdir, pkg, ci, chunk); err != nil {
			return err
		}
	}
	return nil
}

func emitChunk(a *Analysis, pdir, pkg string, ci int, fns []*FnCode) error {
	a.curPkg = pkg
	defer func() { a.curPkg = "" }()

	var body bytes.Buffer
	fmt.Fprintf(&body, "var regs%d = []reg{\n", ci)
	for _, fc := range fns {
		fmt.Fprintf(&body, "\t{0x%08X, %d, %s},\n", fc.Fn.Addr, fc.End-fc.Fn.Addr, a.Names[fc.Fn.Addr])
		for _, e := range fc.Entries {
			fmt.Fprintf(&body, "\t{0x%08X, 0, func(c *g.CPU) { %s_e(c, 0x%08X) }},\n", e.Addr, a.Names[fc.Fn.Addr], e.Addr)
		}
	}
	fmt.Fprintln(&body, "}")
	fmt.Fprintln(&body)

	for _, fc := range fns {
		if err := emitFn(a, &body, fc); err != nil {
			return fmt.Errorf("%s: %w", fc.Fn.Name, err)
		}
	}

	var w bytes.Buffer
	genHeader(&w, a.PkgIdent[pkg])
	fmt.Fprintln(&w, `import g "dbut.dev/sapphire/gba"`)
	fmt.Fprintln(&w)
	w.Write(body.Bytes())
	return writeGo(filepath.Join(pdir, fmt.Sprintf("chunk%03d.go", ci)), w.Bytes())
}

func (a *Analysis) samePkg(t uint32) bool {
	return a.Pkg[t] == a.curPkg
}

func (a *Analysis) callExpr(t uint32, tmode Mode) string {
	if fc := a.ByAddr[t]; fc != nil && a.samePkg(t) {
		return fmt.Sprintf("%s(c)", a.Names[t])
	}
	if fc := a.containing(t); fc != nil && fc.hasEntry(t) && a.samePkg(fc.Fn.Addr) {
		return fmt.Sprintf("%s_e(c, 0x%08X)", a.Names[fc.Fn.Addr], t)
	}
	if tmode == ModeThumb {
		return fmt.Sprintf("c.NCallT(0x%08X)", t)
	}
	return fmt.Sprintf("c.NCallArm(0x%08X)", t)
}

func emitFn(a *Analysis, w io.Writer, fc *FnCode) error {
	name := a.Names[fc.Fn.Addr]
	multi := len(fc.Entries) > 0

	fmt.Fprintf(w, "// 0x%08X\n", fc.Fn.Addr)
	if multi {
		fmt.Fprintf(w, "func %s(c *g.CPU) { %s_e(c, 0) }\n\n", name, name)
		fmt.Fprintf(w, "func %s_e(c *g.CPU, e uint32) {\n", name)
	} else {
		fmt.Fprintf(w, "func %s(c *g.CPU) {\n", name)
	}
	fmt.Fprintln(w, "\tlr := c.R[14]")
	fmt.Fprintln(w, "\t_ = lr")

	hasJT := false
	for _, addr := range fc.Order {
		if fc.Insts[addr].Kind == KMovPC {
			hasJT = true
		}
	}
	if hasJT {
		fmt.Fprintln(w, "\tvar jt uint32")
	}

	labels := map[uint32]bool{}
	for l := range fc.Labels {
		if _, ok := fc.Insts[l]; ok {
			labels[l] = true
		}
	}
	if multi {
		labels[fc.Fn.Addr] = true
		fmt.Fprintln(w, "\tswitch e {")
		fmt.Fprintf(w, "\tcase 0:\n\t\tgoto L_%08X\n", fc.Fn.Addr)
		for _, e := range fc.Entries {
			if _, ok := fc.Insts[e.Addr]; !ok {
				return fmt.Errorf("entry %08X not decoded", e.Addr)
			}
			fmt.Fprintf(w, "\tcase 0x%08X:\n\t\tgoto L_%08X\n", e.Addr, e.Addr)
		}
		fmt.Fprintln(w, "\t}")
	}

	leaders := map[uint32]bool{fc.Fn.Addr: true}
	for l := range labels {
		leaders[l] = true
	}
	blockCost := map[uint32]uint32{}
	var curLeader uint32
	var prevEnd uint32
	for _, addr := range fc.Order {
		in := fc.Insts[addr]
		if addr < prevEnd {
			return fmt.Errorf("overlapping instruction at %08X", addr)
		}
		if addr > prevEnd && prevEnd != 0 {
			leaders[addr] = true
		}
		prevEnd = addr + in.Size
		if leaders[addr] {
			curLeader = addr
		}
		cost := uint32(2)
		if in.Mode == ModeArm {
			cost = 4
		}
		blockCost[curLeader] += cost
	}

	prevEnd = 0
	prevTerm := true
	prevMode := ModeThumb
	for _, addr := range fc.Order {
		in := fc.Insts[addr]
		if addr > prevEnd && prevEnd != 0 && !prevTerm {
			fmt.Fprintf(w, "\t%s\n\treturn\n", a.callExpr(prevEnd, prevMode))
		}
		if labels[addr] {
			fmt.Fprintf(w, "L_%08X:\n", addr)
		}
		if leaders[addr] {
			fmt.Fprintf(w, "\tc.NT(%d)\n", blockCost[addr])
		}
		if err := emitInst(a, w, fc, in); err != nil {
			return err
		}
		prevEnd = addr + in.Size
		prevTerm = isTerminator(in)
		prevMode = in.TMode
	}
	if !prevTerm {
		fmt.Fprintf(w, "\t%s\n\treturn\n", a.callExpr(prevEnd, prevMode))
	}

	if hasJT {
		fmt.Fprintln(w, "Ldispatch:")
		fmt.Fprintln(w, "\tswitch jt {")
		for _, t := range fc.JTTargets {
			fmt.Fprintf(w, "\tcase 0x%08X:\n\t\tgoto L_%08X\n", t, t)
		}
		fmt.Fprintln(w, "\tdefault:")
		fmt.Fprintf(w, "\t\tt := jt | 1\n\t%s\n", indentBlock(indirectTail("c.NCallT(t)")))
		fmt.Fprintln(w, "\t}")
	}

	fmt.Fprintln(w, "}")
	fmt.Fprintln(w)
	return nil
}

func indirectTail(call string) string {
	return fmt.Sprintf("\t\tif t&^1 == lr&^1 {\n\t\t\treturn\n\t\t}\n\t\tif !c.NReg(t) {\n\t\t\tc.U = t\n\t\t\treturn\n\t\t}\n\t\tret := c.R[14]\n\t\t%s\n\t\tif c.U != 0 {\n\t\t\treturn\n\t\t}\n\t\tif ret&^1 == lr&^1 {\n\t\t\treturn\n\t\t}\n\t\tif !c.NReg(ret) {\n\t\t\tc.U = ret\n\t\t\treturn\n\t\t}\n\t\tc.NCallA(ret)\n\t\treturn", call)
}

func indentBlock(s string) string {
	return strings.ReplaceAll(s, "\n", "\n\t")
}

func isTerminator(in *Inst) bool {
	switch in.Kind {
	case KFarJump, KMovPC, KPopPC, KTrap:
		return true
	case KBranch, KBX, KLdrPCStatic, KLdmPC, KAluPC:
		return in.Mode == ModeThumb || in.Cond == 0xE
	}
	return false
}

func emitInst(a *Analysis, w io.Writer, fc *FnCode, in *Inst) error {
	A := in.Addr
	thumb := in.Mode == ModeThumb

	switch in.Kind {
	case KGeneric:
		if thumb {
			if sem, ok := thumbSemantic(in.Raw, A); ok {
				io.WriteString(w, sem)
				break
			}
			if in.ReadsPC {
				fmt.Fprintf(w, "\tc.R[15] = 0x%08X\n", A+4)
			}
			fmt.Fprintf(w, "\tc.Thumb(0x%04X)\n", in.Raw)
		} else {
			fmt.Fprintf(w, "\tc.R[15] = 0x%08X\n", A+8)
			fmt.Fprintf(w, "\tc.Arm(0x%08X)\n", in.Raw)
		}

	case KSWI:
		var nn uint32
		if thumb {
			nn = in.Raw & 0xFF
		} else {
			nn = in.Raw >> 16 & 0xFF
		}
		if !thumb && in.Cond != 0xE {
			fmt.Fprintf(w, "\tif c.Cond(0x%X) {\n\t\tc.NSWI(0x%02X)\n\t}\n", in.Cond, nn)
		} else {
			fmt.Fprintf(w, "\tc.NSWI(0x%02X)\n", nn)
		}

	case KCondBranch:
		inFn := in.Target >= fc.Fn.Addr && in.Target < fc.End
		if inFn {
			fmt.Fprintf(w, "\tif c.Cond(0x%X) {\n\t\tgoto L_%08X\n\t}\n", in.Cond, in.Target)
		} else {
			fmt.Fprintf(w, "\tif c.Cond(0x%X) {\n\t\t%s\n\t\treturn\n\t}\n", in.Cond, a.callExpr(in.Target, in.TMode))
		}

	case KBranch:
		inFn := in.Target >= fc.Fn.Addr && in.Target < fc.End
		if inFn {
			if _, decoded := fc.Insts[in.Target]; !decoded {
				fmt.Fprintf(w, "\tc.NTrap(0x%08X)\n", A)
				break
			}
			if !thumb && in.Cond != 0xE {
				fmt.Fprintf(w, "\tif c.Cond(0x%X) {\n\t\tgoto L_%08X\n\t}\n", in.Cond, in.Target)
			} else {
				fmt.Fprintf(w, "\tgoto L_%08X\n", in.Target)
			}
		} else {
			call := a.callExpr(in.Target, in.TMode)
			if !thumb && in.Cond != 0xE {
				fmt.Fprintf(w, "\tif c.Cond(0x%X) {\n\t\t%s\n\t\treturn\n\t}\n", in.Cond, call)
			} else {
				fmt.Fprintf(w, "\t%s\n\treturn\n", call)
			}
		}

	case KFarJump:
		fmt.Fprintf(w, "\tc.R[14] = 0x%08X\n", (A+4)|1)
		fmt.Fprintf(w, "\tgoto L_%08X\n", in.Target)

	case KBranchLink:
		call := a.callExpr(in.Target, in.TMode)
		ret := A + 4
		if thumb {
			ret |= 1
		}
		body := fmt.Sprintf("c.R[14] = 0x%08X\n\t%s\n\tif c.U != 0 {\n\t\tif c.U != 0x%08X {\n\t\t\treturn\n\t\t}\n\t\tc.U = 0\n\t}", ret, call, ret)
		if !thumb && in.Cond != 0xE {
			body = fmt.Sprintf("if c.Cond(0x%X) {\n\t%s\n\t}", in.Cond, indentBlock(body))
		}
		fmt.Fprintf(w, "\t%s\n", body)

	case KBX:
		body := fmt.Sprintf("t := c.R[%d]\n%s", in.Reg, indirectTail("c.NCallA(t)"))
		if !thumb && in.Cond != 0xE {
			fmt.Fprintf(w, "\tif c.Cond(0x%X) {\n\t\t%s\n\t}\n", in.Cond, body)
		} else {
			fmt.Fprintf(w, "\t{\n\t\t%s\n\t}\n", body)
		}

	case KMovPC:
		fmt.Fprintf(w, "\tjt = c.R[%d] &^ 1\n\tgoto Ldispatch\n", in.Reg)

	case KPopPC:
		if in.Reg != 0 {
			if sem, ok := thumbSemantic(0xBC00|in.Reg, A); ok {
				io.WriteString(w, sem)
			} else {
				fmt.Fprintf(w, "\tc.Thumb(0x%04X)\n", 0xBC00|in.Reg)
			}
		}
		body := fmt.Sprintf("t := c.Memory.Read32(c.R[13], true, false)\n\t\tc.R[13] += 4\n%s", indirectTail("c.NCallT(t)"))
		fmt.Fprintf(w, "\t{\n\t\t%s\n\t}\n", body)

	case KLdmPC, KAluPC:
		body := fmt.Sprintf("c.R[15] = 0x%08X\n\t\tc.Arm(0x%08X)\n\t\tt := c.NPC()\n%s", A+8, in.Raw, indirectTail("c.NCallKeep(t)"))
		if in.Cond != 0xE {
			fmt.Fprintf(w, "\tif c.Cond(0x%X) {\n\t\t%s\n\t}\n", in.Cond, body)
		} else {
			fmt.Fprintf(w, "\t{\n\t\t%s\n\t}\n", body)
		}

	case KLdrPCStatic:
		fmt.Fprintf(w, "\tc.NCallArm(0x%08X)\n\treturn\n", in.Target)

	case KTrap:
		fmt.Fprintf(w, "\tc.NTrap(0x%08X)\n", A)

	default:
		return fmt.Errorf("unhandled kind %d at %08X", in.Kind, A)
	}
	return nil
}
